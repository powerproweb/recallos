def run_onboarding(
    directory: str = ".",
    config_dir: Path = None,
    auto_detect: bool = True,
    interactive: bool = True,
    mode: str = None,
    people: list = None,
    projects: list = None,
    domains: list = None,
) -> EntityRegistry:
    """
    Run the full onboarding flow.
    Returns the seeded EntityRegistry.

    When interactive=False, uses the provided mode/people/projects/domains
    (or defaults) and skips all input() prompts.  Safe for service/API callers.
    """
    if not interactive:
        mode = mode or "combo"
        people = people or []
        aliases = {}
        projects = projects or []
        domains = domains or DEFAULT_DOMAINS[mode]
    else:
        # Step 1: Mode
        mode = _ask_mode()

        # Step 2: People
        people, aliases = _ask_people(mode)

        # Step 3: Projects
        projects = _ask_projects(mode)

        # Step 4: Domains (stored in config, not registry -- just show user)
        domains = _ask_domains(mode)

    # Step 5: Auto-detect additional people from files
    if (
        interactive
        and auto_detect
        and _yn("\nScan your files for additional names we might have missed?")
    ):
        directory = _ask("Directory to scan", default=directory)
        detected = _auto_detect(directory, people)
        if detected:
            _hr()
            print(f"\n  Found {len(detected)} additional name candidates:\n")
            for e in detected:
                print(
                    f"    {e['name']:20} confidence={e['confidence']:.0%}  "
                    f"({', '.join(e['signals'][:1])})"
                )
            print()
            if _yn("  Add any of these to your registry?"):
                for e in detected:
                    ans = input(f"    {e['name']} Ã¢â‚¬â€ (p)erson, (s)kip? ").strip().lower()
                    if ans == "p":
                        rel = input(f"    Relationship/role for {e['name']}? ").strip()
                        ctx = (
                            "personal"
                            if mode == "personal"
                            else (
                                "work"
                                if mode == "work"
                                else input("    Context Ã¢â‚¬â€ (p)ersonal or (w)ork? ")
                                .strip()
                                .lower()
                                .replace("w", "work")
                                .replace("p", "personal")
                            )
                        )
                        people.append({"name": e["name"], "relationship": rel, "context": ctx})

    # Step 6: Warn about ambiguous names (skip printing when non-interactive)
    ambiguous = _warn_ambiguous(people)
    if interactive and ambiguous:
        _hr()
        print(f"""
  Heads up Ã¢â‚¬â€ these names are also common English words:
    {", ".join(ambiguous)}

  recallos will check the context before treating them as person names.
  For example: "I picked up Riley" Ã¢â€ â€™ person.
               "Have you ever tried" Ã¢â€ â€™ adverb.
""")

    # Build and save registry
    registry = EntityRegistry.load(config_dir)
    registry.seed(mode=mode, people=people, projects=projects, aliases=aliases)

    # Generate RecallScript entity registry + critical facts bootstrap
    _generate_recallscript_bootstrap(people, projects, domains, mode, config_dir)

    # Summary
    _header("Setup Complete")
    print()
    print(f"  {registry.summary()}")
    print(f"\n  Domains: {', '.join(domains)}")
    print(f"\n  Registry saved to: {registry._path}")
    print("\n  RecallScript entity registry: ~/.recallos/recallscript_entities.md")
    print("  Critical facts bootstrap: ~/.recallos/critical_facts.md")
    print("\n  Your AI will know your world from the first session.")
    print()

    return registry


