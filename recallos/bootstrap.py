def _generate_recallscript_bootstrap(
    people: list, projects: list, domains: list, mode: str, config_dir: Path = None
):
    """
    Generate RecallScript entity registry + critical facts bootstrap from onboarding data.
    These files teach the AI about the user's world from session one.
    """
    recallos_dir = Path(config_dir) if config_dir else Path.home() / ".recallos"
    recallos_dir.mkdir(parents=True, exist_ok=True)

    # Build AAAK entity codes (first 3 letters of name, uppercase)
    entity_codes = {}
    for p in people:
        name = p["name"]
        code = name[:3].upper()
        # Handle collisions
        while code in entity_codes.values():
            code = name[:4].upper()
        entity_codes[name] = code

    # RecallScript entity registry
    registry_lines = [
        "# RecallScript Entity Registry",
        "# Auto-generated by recallos init. Update as needed.",
        "",
        "## People",
    ]
    for p in people:
        name = p["name"]
        code = entity_codes[name]
        rel = p.get("relationship", "")
        registry_lines.append(f"  {code}={name} ({rel})" if rel else f"  {code}={name}")

    if projects:
        registry_lines.extend(["", "## Projects"])
        for proj in projects:
            code = proj[:4].upper()
            registry_lines.append(f"  {code}={proj}")

    registry_lines.extend(
        [
            "",
            "## RecallScript Quick Reference",
            "  Symbols: \u2665=love \u2605=importance \u26a0=warning \u2192=relationship |=separator",
            "  Structure: KEY:value | GROUP(details) | entity.attribute",
            "  Read naturally -- expand codes, treat *markers* as emotional context.",
        ]
    )

    (recallos_dir / "recallscript_entities.md").write_text("\n".join(registry_lines))

